public class Invention{
	private String name;
	private int age;
	private String placeOfOrigin;
	
	public Invention(String name, int age, String placeOfOrigin) {
		this.name = name;
		this.age = age;
		this.placeOfOrigin = placeOfOrigin;
	}
	
	public String getName() {
		return name;
	}
	
	public int getAge() {
		return age;
	}
	
	public String getPlaceOfOrigin() {
		return placeOfOrigin;
	}
}

